#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*ptr;

	if (!(ptr = malloc(count * size)))
		return (NULL);
	ptr = ft_memset(ptr, '\0', (count * size));
	return (ptr);
}
